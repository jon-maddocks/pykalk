#Add two numbers and return the result
def doAdd(num1, num2):
    answer = num1 + num2
    return answer

#Subtract two number and return the result
def doSub(num1, num2):
    answer = num1 - num2
    return answer

#Multiply two numbers and return the result
def doMul(num1, num2):
    answer = num1 * num2
    return answer

#Divide two numbers and return the result
def doDivide(num1, num2):
    answer = num1 / num2
    return answer